namespace StockIndicators;

/// <summary>
/// Defines the available colors for chart elements.
/// </summary>
public enum ChartColor
{
    /// <summary>
    /// Random color
    /// </summary>
    Random,

    /// <summary>
    /// Positive value color (usually green)
    /// </summary>
    PositiveValue,

    /// <summary>
    /// Negative value color (usually red)
    /// </summary>
    NegativeValue,

    /// <summary>
    /// Light Gray
    /// </summary>
    LightGray,

    /// <summary>
    /// Strong Gray
    /// </summary>
    StrongGray,

    /// <summary>
    /// Black
    /// </summary>
    Black,

    /// <summary>
    /// Red
    /// </summary>
    Red,

    /// <summary>
    /// Neutral
    /// </summary>
    Neutral
}
